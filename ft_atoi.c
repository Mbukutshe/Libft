#include "Libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int num;

	i = -1;
	sign = 1;
	num = 0;
	while (str[++i] != '\0')
	{
		if (str[i] == '-')
			sign = -1;
		if (str[i] >= '0' && str[i] <= '9')
			num = (num * 10) + (str[i] - '0');
	}
	return (num * sign);
}
